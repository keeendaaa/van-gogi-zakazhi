export const imageMap: Record<string, string> = imageMapData;

// Функция для получения URL изображения по названию блюда
export function getImageUrl(itemName: string): string {
  const url = imageMap[itemName] || '';
  if (!url) return '';
  
  // Кодируем URL для правильной работы с кириллицей в именах файлов
  // Разделяем на baseUrl и путь к файлу
  const parts = url.split('/menu/');
  if (parts.length === 2) {
    const baseUrl = parts[0];
    const fileName = parts[1];
    // Кодируем только имя файла
    const encodedFileName = encodeURIComponent(fileName);
    return `${baseUrl}/menu/${encodedFileName}`;
  }
  
  return url;
}
